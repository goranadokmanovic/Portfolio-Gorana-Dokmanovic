(function () {
  const STORAGE_KEY = "mynestof4-lang";
  const MOBILE_MQ = window.matchMedia("(max-width: 768px)");
  let currentLang = localStorage.getItem(STORAGE_KEY) || "sr";

  function resolveTranslation(lang, key) {
    if (!translations[lang]) return null;
    const mobileKey = `${key}_mobile`;
    if (MOBILE_MQ.matches && translations[lang][mobileKey]) {
      return translations[lang][mobileKey];
    }
    return translations[lang][key] ?? null;
  }

  function applyLanguage(lang) {
    currentLang = lang;
    localStorage.setItem(STORAGE_KEY, lang);
    document.documentElement.lang = lang;

    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      const text = resolveTranslation(lang, key);
      if (text) {
        el.textContent = text;
      }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      const key = el.getAttribute("data-i18n-placeholder");
      const text = resolveTranslation(lang, key);
      if (text) {
        el.placeholder = text;
      }
    });

    document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
      const key = el.getAttribute("data-i18n-aria");
      const text = resolveTranslation(lang, key);
      if (text) {
        el.setAttribute("aria-label", text);
      }
    });

    document.querySelectorAll(".lang-option").forEach((opt) => {
      opt.classList.toggle("lang-option--active", opt.dataset.lang === lang);
    });
  }

  const toggle = document.getElementById("lang-toggle");
  if (toggle) {
    toggle.addEventListener("click", (e) => {
      const clicked = e.target.closest("[data-lang]");
      if (clicked && clicked.dataset.lang !== currentLang) {
        applyLanguage(clicked.dataset.lang);
      } else {
        applyLanguage(currentLang === "sr" ? "en" : "sr");
      }
    });
  }

  applyLanguage(currentLang);

  MOBILE_MQ.addEventListener("change", () => {
    applyLanguage(currentLang);
  });
})();
